# Arithmethic Operators
a = 10
b = 20
add_result = a + b
sub_result = a - b
mult_result = a * b
div_result = a / b
floor_div_result = a // b
modulus_result = a % b
exp_result = a**b

print(add_result)
print(sub_result)
print(mult_result)
print(div_result)
print(floor_div_result)
print(modulus_result)
print(exp_result)

# Comparison Operators
a = 10
b = 10

# equal to ==
compare_result = a == b
print(compare_result)

str1 = "Rohit"
str2 = "Rohit"
compare_result = str1 == str2
print(compare_result)

# not equal to !=
compare_result = a != b
print(compare_result)
compare_result = str1 != str2
print(compare_result)

# greater than >
compare_result = a > b
print(compare_result)

# less than <
compare_result = a < b
print(compare_result)

# greater than or equal to >=
compare_result = a >= b
print(compare_result)

# less than or equal to <=
compare_result = a <= b
print(compare_result)

# Logical Operators
a = True
b = False

# and
print(a and b)
print(a or a)
print(not b)
