# This is a single line comment

# Case Sensitivity- Python is case sensitive
Name = "Rohit"
name = "rohit"

print(Name)
print(name)

# Intendation- Python uses indentation to define code blocks, instead of brackets
age = 20
if age > 18:
    print("You are an adult")

# Line Continuation
total = 1 + 2 + 3 + 4 + 5
print(total)

# Variable assignment
age = 20
name = "Rohit"
print(type(age))
print(type(name))


# Type Inference
variable = 20
print(type(variable))
variable = "Rohit"
print(type(variable))
